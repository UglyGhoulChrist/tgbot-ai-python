# Запрашиваем у пользователя целое число
user_input = input("Введите целое число: ")

# Попытка преобразовать введенное значение в целое число
try:
    number = int(user_input)
    # Проверяем, является ли число четным или нечетным
    if number % 2 == 0:
        print(f"Число {number} является четным.")
    else:
        print(f"Число {number} является нечетным.")
except ValueError:
    # Выводим сообщение об ошибке, если введенное значение не является целым числом
    print("Это не целое число. Пожалуйста, введите целое число.")